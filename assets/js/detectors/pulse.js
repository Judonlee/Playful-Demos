(function(PM, _) {

	var DEBUG = true;

	var Pulse = function(options) {
		this.init(options);
	};

	if(typeof PM === 'undefined') {
		window.Pulse = Pulse; // 
	} else {
		PM.Pulse = function(options) {
			return new Pulse(options); // 
		};
	}

	Pulse.prototype = {
		init: function(options) {
			// The default options
			this.options = {
				id: '', // The id of messages being broadcast.
				probe: null, // A Probe instance.
				context: null
			};
			// Read in instancing options.
			for(var option in options) {
				if(options.hasOwnProperty(option)) {
					this.options[option] = options[option];
				}
			}

			// The Web Audio API context
			this.context = PM && PM.context ? PM.context : this.options.context;
		},
		broadcast: function(type) {
			// Broadcast the message
			if(PM) {
				PM.broadcast(type, {
					id: this.options.id,
					target: this,
					msg: 'Generated by: Pulse'
				});
			}
		}
	};
	return Pulse;
}(window.PM, window._));
